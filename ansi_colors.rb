module ANSIColors
  RESET = "\e[0m"
  BOLD = "\e[1m"
  ITALIC = "\e[3m"
  UNDERLINE = "\e[4m"

  # Text color codes
  BLACK = "\e[30m"
  RED = "\e[31m"
  GREEN = "\e[32m"
  YELLOW = "\e[33m"
  BLUE = "\e[34m"
  MAGENTA = "\e[35m"
  CYAN = "\e[36m"
  WHITE = "\e[37m"

  # Background color codes
  BG_BLACK = "\e[40m"
  BG_RED = "\e[41m"
  BG_GREEN = "\e[42m"
  BG_YELLOW = "\e[43m"
  BG_BLUE = "\e[44m"
  BG_MAGENTA = "\e[45m"
  BG_CYAN = "\e[46m"
  BG_WHITE = "\e[47m"
end
